package br.com.mhdev.socialpovo.model;

public class Estado {

	private int id;
	private String nome;
	private String uf;
	public int getId() {
	    return this.id;
	}
	public void setId(int id) {
	    this.id = id;
	}
	public String getNome() {
	    return this.nome;
	}
	public void setNome(String nome) {
	    this.nome = nome;
	}
	public String getUf() {
	    return this.uf;
	}
	public void setUf(String uf) {
	    this.uf = uf;
	}
	
	
}
